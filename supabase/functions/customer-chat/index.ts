import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const systemPrompt = `You are an advanced AI Customer Service Chatbot built using NLP and Deep Learning.
Your goal is to provide polite, helpful, and human-like responses to customer queries.

BEHAVIOR GUIDELINES:
- Reply in a friendly, calm, and professional tone
- Give concise + helpful answers, NOT long essays
- Always offer a solution or next step
- Detect sentiment and respond appropriately:
  - Angry → Apologize + provide quick resolution
  - Confused → Explain clearly and simply
  - Happy → Respond positively and warmly

CUSTOMER SERVICE SKILLS:
- Handle queries about: Order status, Refunds, Account problems, Technical support, Product Info
- If the query is unclear, politely ask for more details
- Never guess sensitive information like order numbers or account details

OUTPUT FORMAT - Always structure your response as:
✔ **Understanding**: [Brief restatement of the issue]
✔ **Solution**: [Your helpful response/solution]
✔ **Next Step**: [Clear action item or follow-up question]

Be empathetic, efficient, and always aim to resolve the customer's concern in the first response when possible.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Processing chat request with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat function error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
